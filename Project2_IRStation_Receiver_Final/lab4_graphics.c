void Check_Bit(void){ // Assign specific bit from array a 1 or skip and leave it as 0
  uint32_t time;
	if(index==0){
    time = 22;
		BitTime=54;
	}else{
    time = BitTime;
		BitTime=BitTime+32;
	}
	for(int i=time; i<(BitTime+2); i++){
		if(AddrCmd[i]==0){
			Bit[index]=1;
			BitTime=BitTime+60;
		}
	}
	if(index>=5){
		StartF=0;
		GPIO_PORTD_IM_R |= 0x01;
		GPIO_PORTD_ICR_R |= 0x01;
		IR_Count = 0;
	}
}

void Timer1A_Init(void){ 
	SYSCTL_RCGCTIMER_R |= 0x02;	              // Enable Timer 1A
	TIMER1_CTL_R = 0;									        // Disable Timer 1A
	TIMER1_CFG_R |= 0x04;								      // 16-bit
	TIMER1_TAMR_R |= 0x02;							      // Periodic countdown mode
	TIMER1_TAPR_R = 2000;                     // 80MHz/2kHz = 40Hz
	TIMER1_TAILR_R = 1;							      
	TIMER1_ICR_R |= 0x01;                     // Clear Flag
	TIMER1_IMR_R |= 0x01;								      // Enable Interrupt Mask 
	TIMER1_CTL_R |= 0x01;								      // Enable Timer1A
	NVIC_PRI5_R = (NVIC_PRI5_R & 0xffffff) | 0xc0000000;
	NVIC_EN0_R |= 0x00200000;	                // Enable IRQ21
}

void PortD_Init(void){
	volatile uint32_t delay;
	SYSCTL_RCGCGPIO_R |= 0x08;  					    // Port D
	delay = SYSCTL_RCGCGPIO_R;   					    // Allow time for clock to start
	GPIO_PORTD_DIR_R &= ~0x01;   					    // PD0 in
	GPIO_PORTD_AFSEL_R &= ~0x01;              // Disable alt funct on PD0
	GPIO_PORTD_DEN_R |= 0x01;   		  			  // Enable digital I/O on PD0
	GPIO_PORTD_PCTL_R &= ~0x0000000F;         // Disable port control
	GPIO_PORTD_AMSEL_R &= ~0x01;              // Disable analog functionality on PD0
	GPIO_PORTD_IS_R &= ~0x01;                 // Edge-Sensitive
	GPIO_PORTD_IBE_R &= ~0x01;                // Not Both Edges
	GPIO_PORTD_IEV_R &= ~0x01;                // Falling Edge event
	GPIO_PORTD_ICR_R |= 0x01;                 // Clear Interrupt flag
	GPIO_PORTD_IM_R |= 0x01;                  // Interrupt Mask
}

void GPIOPortD_Handler(void){               // Disables interrupt when signal received
	volatile int readback;
	GPIO_PORTD_IM_R &= ~0x01;
	GPIO_PORTD_ICR_R &= ~0x01;
	StartF = 1;                               // Get ready to check start bit
}

void Timer1A_Handler(void){
	volatile uint32_t readback;
	if(TIMER1_MIS_R & 0x01){                  // Check Mask Interrupt
        if(StartF){
          if(IR_Count>=60){
            AddrCmd[IR_Count-60] = IR_in;
          }
          if((IR_Count>=45)&&(IR_Count<56)&&(!IR_in)){ // Check for Start Bit Errors
            IR_Count=0;
          }else if((IR_Count>=56)&&(IR_Count<60)&&(IR_in)){
            IR_Count=0;
          }else{
            IR_Count++;                     // Count Time Elapse (25us each time into Handler)
          }
        }
    TIMER1_ICR_R = 0x01;                    // Clear TimerA Time-Out Raw Interrupt(TATORIS) bit in RIS/MIS registers
  }else{
    TIMER1_ICR_R = TIMER1_MIS_R;            // Clear all other but TATORIS bit
  }
  readback = TIMER1_ICR_R;
}

